    private void jTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMouseClicked
        
        
         DefaultTableModel model = (DefaultTableModel)jTable.getModel();
         int SelectedRows = jTable.getSelectedRow();
         
           tfstudent.setText(model.getValueAt(SelectedRows, 0).toString());
           tffirst.setText(model.getValueAt(SelectedRows, 1).toString());
           tfsur.setText(model.getValueAt(SelectedRows, 2).toString());
           tfmobile.setText(model.getValueAt(SelectedRows, 3).toString());
        
        
    }//GEN-LAST:event_jTableMouseClicked

