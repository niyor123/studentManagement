    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
      
        String id = tfstudent.getText();
        String firstname = tffirst.getText();
        String surname = tfsur.getText();
        String mobile = tfmobile.getText();
       
        
        
        if(id.isEmpty() || firstname.isEmpty() || surname.isEmpty() ||  mobile.isEmpty()) {
             JOptionPane.showMessageDialog(null,"Please enter all fields",
                     "Try Again",JOptionPane.ERROR_MESSAGE);
           
        } else {
            DefaultTableModel model = (DefaultTableModel) jTable.getModel();
            model.addRow(new Object[] {id, firstname, surname, mobile});
            
            tfstudent.setText("");
            tffirst.setText("");
            tfsur.setText("");
            tfmobile.setText("");
            
            
        }
        
        
        
        
        
    }//GEN-LAST:event_btnaddActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        
         
        int row = jTable.getSelectedRow();
         if(row < 0) {
            JOptionPane.showMessageDialog(null,"No Row is selected","Select Row",JOptionPane.ERROR_MESSAGE);
        } else {
                  DefaultTableModel model = (DefaultTableModel) jTable.getModel();
                  model.removeRow(row);
        }
        
        
    }//GEN-LAST:event_btndeleteActionPerformed

